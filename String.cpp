// Constructor used to initialize a string test
String::String(void)
{
  s_capacity=12;
  s_length=6;
  char* s_data = new char[s_capacity];
  for (int i=0; i<=s_length;i+=3)
    {
			s_data[i]='c';
			s_data[i+1]='o';
			s_data[i+2]='u';
    }
}

String::String(const char* s)
{

  //Function that allows to know the length of s.
  int i=0;
  while(s[i]!='\0')
	{
		i++;
	 }
   
  //Function that copies the character sequence pointed by s.
  s_data = new char[i];
	for (int j=0; j<=i; j++)
	{
		s_data[j]=s[j];
	}
  
  s_length=i+1;
}

// ===========================================================================
//                                  Destructor
// ===========================================================================
String::~String(void)
{
    delete [] s_data;
    
    
}

// ===========================================================================
//                                 Public Methods
// ===========================================================================
 bool String::empty(void) const
 {
  return (s_length==0);
 }


 void String::resize (unsigned int n, char c)
 {
	 if (n<s_length)	
	 {
		 for (int i=n; i<s_length; i++) 
     { 
    	 s_data[i]=NULL;
     }
	  	s_length=n;
	 } 

	 if (n>s_length)
	 {
    for (int i=s_length; i<n; i++) 
     { 
    	 s_data[i]=c;
     }
    s_length=n;
	 }
}

